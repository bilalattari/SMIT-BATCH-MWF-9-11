// In JavaScript, comparison involves evaluating two values and determining their relationship.
// The result of a comparison is a boolean value (true or false).
// JavaScript provides comparison operators to perform these evaluations:

// Equal to (==): Checks if two values are equal. Note that this operator performs type coercion,
// so it may not always behave as expected.
// Not equal to (!=): Checks if two values are not equal.
// Strict equal to (===): Checks if two values are equal, including their types.
// Strict not equal to (!==): Checks if two values are not equal, including their types.
// Greater than (>): Checks if the left operand is greater than the right operand.
// Less than (<): Checks if the left operand is less than the right operand.
// Greater than or equal to (>=): Checks if the left operand is greater than or equal to the right operand.
// Less than or equal to (<=): Checks if the left operand is less than or equal to the right operand.
