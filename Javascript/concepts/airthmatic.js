// In JavaScript, arithmetic involves performing mathematical operations on numerical values.
//The basic arithmetic operators include:

// Addition (+): Adds two or more numbers together.
// Subtraction (-): Subtracts the second number from the first.
// Multiplication (*): Multiplies two or more numbers.
// Division (/): Divides the first number by the second.
// Modulus (%): Returns the remainder after division (useful for checking if a number is even or odd).
// Increment (++): Adds one to the current value of a variable.
// Decrement (--): Subtracts one from the current value of a variable.

var num1 = 20
var num2 = 10
console.log(num1 + num2) //Addition

console.log(num1 - num2) //Subtraction

console.log(num1 * num2) //Multiply

console.log(num1 / num2) //Divide

console.log(23 % num2) //Modulus

console.log(++num1)
 //increment
console.log(--num1) //increment
